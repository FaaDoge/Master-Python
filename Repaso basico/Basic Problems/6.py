# Problem: Determine if `lista` has more than 5 elements and if the first element is greater than 10.
lista = [10, 23, 1, 3, 54, 24]
if len(lista) > 5 and lista[0] > 10:
    print("The list has more than 5 elements, and the first element is greater than 10.")
else:
    print("Either the list has 5 or fewer elements, or the first element is 10 or less.")
