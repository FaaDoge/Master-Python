# Problem: Use the values of `entero` and `flotante` to check if they are equal, greater, or lesser.
entero = 21
flotante = 1.2
if entero > flotante:
    print(f"{entero} is greater than {flotante}.")
elif entero < flotante:
    print(f"{entero} is less than {flotante}.")
else:
    print(f"{entero} is equal to {flotante}.")